<?php
//perulangan for 
echo "Peulangan for<br>";
 for ($i=1; $i <= 10 ; $i++) { 
 	echo "$i<br>";
 }

 //perulangan while
 echo "Perulangan while<br>";
 $x = 5;
 while ($x<=15) {
 	echo "$x<br>";
 	$x++;
 }

//perulangan do-while
 echo "Perulangan do-while<br>";
 $y = 30;
 do {
 	echo "$y<br>";
 	$y+=3;
 } while ($y <= 70);

 ?>